export const municipalities 
  = ProvincesWithMunicipalities.flatMap(province => 
      province.municipalities.map(municipality => ({
        value: municipality.code,
        label: municipality.name
      }))
    );

export const municipalitiesOnlyLabel
  = ProvincesWithMunicipalities.flatMap(province => 
      province.municipalities.map(municipality => (municipality.name))
    );

export const getPlaceLabel = (code: string | null | undefined): string => {
  if (!code) return '';

  const province = ProvincesWithMunicipalities.find(p => p.code === code);
  if (province) return province.name;

  for (const p of ProvincesWithMunicipalities) {
    const municipality = p.municipalities.find(m => m.code === code);
    if (municipality) return municipality.name;
  }

  return code;
};